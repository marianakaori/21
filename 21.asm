inputAposta:   ; Rotina que recebe uma palavra para que o segundo usuario tente adivinhar
        ;salva as variaveis anteriores e inicializa as novas
        push fr
        push r0         ; Recebe caracter digitada
        push r1         ; codigo do enter
        push r2         ; Contador de caracteres para o vetor que armazena a string
        push r3         ; ponteiro para aposta
        push r4         ; palavra[r3+r2]
        push r5         ; Tamanho maximo da aposta

        loadn r1, #13           ; codigo enter
        loadn r2, #0            ; inicia r2 = 0
        loadn r3, #apostastring      ; ponteiro para aposta string
        loadn r5, #2          ; Tamanho maximo da aposta (digitos)
        ;----------------


   inputAposta_Loop:
                call digTecla          ; Espera que uma tecla seja digitada e salva na variavel global "Tecla"
                        
                load r0, Tecla                          ; Letra --> r0
                cmp r0, r1                                      ;comparacao se r0 eh enter(13)
                jeq inputAposta_Fim            ; VAi para o fim se Enter!
                
                add r4, r3, r2
                storei r4, r0                           ; palavra[r2] = TEcla

                inc r2
                store tamanhoApostaString, r2                   ; Atualiza o tamanho da palavra
                cmp r2, r5                                              ; verifica se r2 = 3
                jne inputAposta_Loop                   ; Se for, sai, senao goto loop!!
                                
   inputAposta_Fim:
        ; Poe um /0 no final da palavra pra poder imprimir e testar!!
        loadn r0, #0
        add r4, r3, r2
        storei r4, r0                           ; palavra[r2] = /0
        
        pop r5
        pop r4
        pop r3
        pop r2
        pop r1
        pop r0
        pop fr
        rts
        


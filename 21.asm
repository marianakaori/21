                loadi r3, r0
                sub r3, r3, r2
                store aposta, r3
                jmp ApostaStringFim
                
        
        ApostaStringDoisDigitos:
                loadn r1, #10   ;dezena
                
                loadi r3, r0   ; primeiro digito da aposta
                sub r3, r3, r2  ; transforma em int
                mul r3, r3, r1  ; multiplica por 10 por ser a dezena
                
                inc r0          ;avanca o indice
                loadi r4, r0   ;segundo digito da aposta
                sub r4, r4, r2  ; transforma em int
                
                add r4, r3, r4  ; soma os digitos pra ter o numero completo
                store aposta, r4
                
        ApostaStringFim:
                pop r5
                pop r4
                pop r3    
                pop r2
                pop r1
                pop r0
                pop fr
                rts
        
        
